"use client";

import { ShieldAlert, AlertTriangle, CheckCircle2, Clock } from "lucide-react";
import { useQuery } from "@tanstack/react-query";
import axios from "axios";

export default function ActiveAlertsPage() {
  const { data, isLoading: loading } = useQuery({
    queryKey: ["dashboardLive"],
    queryFn: async () => {
      const res = await axios.get("http://localhost:8000/api/v1/dashboard/live");
      return res.data;
    },
    refetchInterval: 10000,
  });

  const alerts = data?.alerts || [];

  return (
    <div className="space-y-6">
      <div>
        <h1 className="text-3xl font-heading font-bold text-slate-900 mb-1">Active Emergency Alerts</h1>
        <p className="text-slate-500 font-medium">Real-time alerts triggered by AI models and hydrological thresholds.</p>
      </div>

      <div className="glass-card rounded-xl bg-white/80 border border-slate-200/60 shadow-sm overflow-hidden">
        <table className="w-full text-left border-collapse">
          <thead>
            <tr className="bg-slate-100 border-b border-slate-200 text-slate-600 uppercase text-xs tracking-wider">
              <th className="p-4 font-bold">Alert Level</th>
              <th className="p-4 font-bold">District</th>
              <th className="p-4 font-bold">Reason</th>
              <th className="p-4 font-bold">Generated By AI</th>
              <th className="p-4 font-bold">Time</th>
              <th className="p-4 font-bold">Status</th>
            </tr>
          </thead>
          <tbody className="divide-y divide-slate-100">
            {loading ? (
              <tr>
                <td colSpan={6} className="p-8 text-center text-slate-500 animate-pulse">
                  Loading Alert Stream...
                </td>
              </tr>
            ) : alerts.length === 0 ? (
              <tr>
                <td colSpan={6} className="p-8 text-center text-emerald-600 font-bold">
                  No active alerts. System optimal.
                </td>
              </tr>
            ) : (
              alerts.map((alert: any) => (
                <tr key={alert.id} className="hover:bg-slate-50 transition-colors">
                  <td className="p-4">
                    <div className="flex items-center space-x-2">
                      {alert.severity === "Severe" || alert.level === "High" ? <AlertTriangle className="w-5 h-5 text-rose-500" /> : null}
                      {alert.severity === "High" || alert.level === "Moderate" ? <ShieldAlert className="w-5 h-5 text-orange-500" /> : null}
                      {alert.severity === "Moderate" || alert.level === "Low" ? <CheckCircle2 className="w-5 h-5 text-emerald-500" /> : null}
                      <span className={`font-bold ${
                        alert.severity === "Severe" || alert.level === "High" ? "text-rose-600" :
                        alert.severity === "High" || alert.level === "Moderate" ? "text-orange-600" :
                        "text-emerald-600"
                      }`}>{alert.severity || alert.level}</span>
                    </div>
                  </td>
                  <td className="p-4 font-bold text-slate-700">{alert.district || "Multiple"}</td>
                  <td className="p-4 text-slate-600 max-w-xs">{alert.message || alert.reason}</td>
                  <td className="p-4">
                    <span className="bg-blue-50 text-blue-700 px-2 py-1 rounded text-xs font-bold border border-blue-200">
                      {alert.generatedBy || "Temporal GDNN Model"}
                    </span>
                  </td>
                  <td className="p-4 text-slate-500 text-sm flex items-center space-x-1">
                    <Clock className="w-4 h-4" /> <span>{alert.issued_at ? new Date(alert.issued_at).toLocaleString() : new Date().toLocaleString()}</span>
                  </td>
                  <td className="p-4">
                    <span className={`px-3 py-1 rounded-full text-xs font-bold ${
                      alert.is_active || alert.status === "Active" ? "bg-rose-100 text-rose-700 border border-rose-200" :
                      "bg-emerald-100 text-emerald-700 border border-emerald-200"
                    }`}>
                      {alert.is_active || alert.status === "Active" ? "Active" : "Monitoring"}
                    </span>
                  </td>
                </tr>
              ))
            )}
          </tbody>
        </table>
      </div>
    </div>
  );
}
